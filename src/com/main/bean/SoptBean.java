package com.main.bean;

/**
 * @author 邓建豪
 * @date 2019/03/31
 * @describe 景点Bean
 */
public class SoptBean {
    private int id;
    private String name;
    private String detail;
    private String img;

    public SoptBean() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
}
